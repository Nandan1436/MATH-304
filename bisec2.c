#include<stdio.h>
#include<math.h>
#define f(x) ((x)*(x)*(x)+4*(x)*(x)-5)

int main(){
	double a,b,e,c,h;
	int i=1,j=1;
	h=10/6+1;
	a=-5,b=a+h,e=10.0e-6;
	/*printf("--------------------------------------------------------------------------------------------------\n");
	printf("Iter        a               b               c               f(a)           f(b)             f(c) \n");
	printf("--------------------------------------------------------------------------------------------------\n");*/
	while(b<=5){
		if(f(a)*f(b)>0){
			a=b;
			b=a+h;
			continue;
		}
		while(1){
			c=(a+b)/2;
			//printf("%d\t %10.6lf\t %10.6lf\t %10.6lf\t %10.6lf\t %10.6lf\t %10.6lf\n",i,a,b,c,f(a),f(b),f(c));
			if(fabs(f(c))<=e){
				//printf("--------------------------------------------------------------------------------------------------\n");
	printf("Approximate root %d = %lf\n",j,c);
	//printf("--------------------------------------------------------------------------------------------------\n");
	j++;
				break;
			}
			if(f(a)*f(c)<0)b=c;
			else a=c;
			i++;
		}
		a=b;
		b=a+h;
	}
	
	return 0;
}
